	<h2>Classify the tables in the Navigation Menu in Groups</h2>
	<ul>
		<li>In the <strong>Navigation Menu</strong>, on the <strong>Left Frame</strong>, you can:<ul>
			<li><strong>Classify</strong> the <strong>Tables</strong> of your database in different <strong>Groups,</strong></li>
			<li><strong>Order</strong> the groups as you want them to appear in the 
		Navigation Menu, and</li>
			<li><strong>Order</strong> the tables within each group. </li>
		</ul>
		</li>
		<li>Follow the following steps:</li>
	</ul>
	<h3>1. Define first the Names of Tables Groups</h3>
	<ul>
		<li>In the text area, on the Top of the page, write the <strong>Group Names</strong> in the order you want them to appear in the <strong>Navigation Menu</strong>. Separate 
		them by a comma, except the last one.<ul>
			<li>For example: <b>Texts, Classifications, ..., Last Group</b></li>
		</ul>
		</li>
		<li>The group <strong>Tools</strong> in the Navigation Menu is included in the program by default. Insert it as the last group in the above text area in order to get it as 
		a tab in the help information you can write for your clients. Don't associate any table to that group.<ul>
			<li>For example: <b>Texts, Classifications, ..., Last Group, Tools</b></li>
		</ul>
		</li>
		<li>If you want to include some <strong>Setup</strong> information to your clients, enter an appropriate name, like <strong>Setup</strong>, as the first group in the above 
		text area. Don't associate any table to that group. It will open as the first tab in the Client's Version of the program.<ul>
			<li>For example: <b>Setup, Texts, Classifications, ..., Last Group, Tools</b></li>
		</ul>
		</li>
		<li><b>Save</b> then your settings in order to get the Group Names in the <strong>Drop-Down</strong> menus that appear in the field <strong>Select Group Name</strong>. </li>
	</ul>
	<h3>2. Define then the Alias Names of Tables</h3>
	<ul>
		<li>Just write the <strong>Name</strong> with which you want every table to appear in <strong>theNavigation Menu</strong> on the Left Frame.</li>
	</ul>
	<h3>3. Choose then a Group Name for every table:</h3>
	<ul>
		<li>Just select from the <strong>drop-down</strong> menu the Group to which you want the current table to belongs to.<ul>
			<li>The <strong>Non Grouped</strong> tables will be placed 
		at the end of the Navigation Menu in alphabetic order.</li>
			<li>However, <strong>Non Grouped</strong> tables will not be Exported to the Clients Program, as they are conceived as <strong>Non Used</strong> tables.</li>
		</ul>
		</li>
	</ul>
	<h3>4. Define Link Names for Add New Records and Updateable Record List</h3>
	<ul>
		<li>For frequently used tables, you may want a link in Navigation Menu that opens directly the form <strong>Add New Records</strong>. Also, you may want a direct link to the
		<strong>Updateable Record List</strong> (see <strong>Related Fields</strong> for more details).</li>
		<li>In those cases, just write in the fields <strong>Add New Record</strong> and <strong>Update In List</strong> the name by which you want the links to appear in the Navigation 
		Menu. The defined links appear just before the alias name of the corresponding table.</li>
		<li>Use these options only for <strong>frequently</strong> used tables.</li>
	</ul>
	<h3>5. Order the Tables within a Group</h3>
	<ul>
		<li>You can define the order of appearance of the tables within every group.</li>
		<li>Click on <strong>Order by Group Name</strong> and then classify the order of appearance of tables within each group by writing numbers or/and letters in the input field besides each table.</li>
		<li>The order mode is descending, and it is applied separately for every group </li>
	</ul>
