<div id="helpTableFields" class="sxHelp text" style="display: none">
    <h3>1. Select Table:</h3>
    <p> Selected a table from the list to get its fields. If you already work with a table, it
        will open automatically when you open this file.</p>
    <h3>2. Visible Fields:</h3>
    <p> Demark the corresponding boxes for the fields that you do not want to appear in the List
        of Recordings. Use it to save space for tables with many fields.</p>
    <h3>3. Order By Criterion:</h3>
    <p> The records are initially ordered by the ID Number of their Primary Key (PK).
        You can select between <b>Descending</b> or <b>Ascending</b> order. You can also
		select another field for <b>initial ordering</b> wich will automatically be sett in <b>Descending</b> order.
		The PK becomes then the secondary criterion of ordering. 
        Click on ONLY PK to return to the PK as unique criterion.</p>
    <h3>4. Alias Name of Fields</h3>
    <p> Write the alias name by which you want the fields to appear in the
        List of Recordings as well as in adding, viewing and editing of records.</p>
    <h3>Notes:</h3>
    <ul>
        <li>The memo fields are excluded automatically from the List of Records (to save space), while the ID Number
            fields (Primary Key Fields) are included automatically.</li>
        <li>&nbsp;All fields are visible when viewing, editing or
            adding records.
        </li>
    </ul>
